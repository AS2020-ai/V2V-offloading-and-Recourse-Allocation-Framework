# OFFLOADING STRATEGIES
# ═══════════════════════════════════════════════════════════════════════════

# A Move is a (task, source_vehicle_idx, target_vehicle_idx) triple.
Move = Tuple[Task, int, int]


def strategy_v2v_baseline(
    vehicles: List[Vehicle],
    nb:       List[List[int]],
    p:        SimParams,
) -> List[Move]:
    """
    Baseline delay-greedy offloading heuristic.

    For each unstarted task, compares the expected local delay against the
    expected delay at every one-hop neighbor (including transmission time).
    Offloads to the neighbor with the lowest total expected delay, if any
    neighbor beats the local estimate.  No urgency weighting, load penalty,
    link-stability check, or energy term.

    Args:
        vehicles  List of active Vehicle objects.
        nb        Neighbor adjacency list from build_neighbors().
        p         SimParams (uses bandwidth, tx_power, noise_floor).

    Returns:
        List of Move tuples for all tasks selected for offloading.
    """
    moves: List[Move] = []
    for vi, v in enumerate(vehicles):
        pending = [tk for tk in v.queue if tk.start_time is None and not tk.locked]
        if not pending or not nb[vi]:
            continue
        for task in pending:
            best_delay, best_target = expected_delay(task, v), vi
            for ui in nb[vi]:
                u    = vehicles[ui]
                d    = vdist(v, u)
                rate = channel_rate(d, p.bandwidth, p.tx_power, p.noise_floor)
                tx   = tx_seconds(task.data_size, rate)
                delay = expected_delay(task, u, tx)
                if delay < best_delay:
                    best_delay, best_target = delay, ui
            if best_target != vi:
                task.locked = True
                moves.append((task, vi, best_target))
    return moves


def strategy_nearest(vehicles: List[Vehicle], nb: List[List[int]]) -> List[Move]:
    """
    Nearest-neighbor offloading strategy.

    Only activates when a vehicle has more than one unstarted task queued.
    Selects the geographically closest neighbor (by Euclidean distance) and
    offloads the first eligible pending task to it.  Makes no assessment of
    neighbor load, link quality, or task deadlines.

    Args:
        vehicles  List of active Vehicle objects.
        nb        Neighbor adjacency list from build_neighbors().

    Returns:
        List of Move tuples for all selected offloads.
    """
    moves: List[Move] = []
    for vi, v in enumerate(vehicles):
        n_waiting = sum(1 for tk in v.queue if tk.start_time is None)
        if n_waiting <= 1 or not nb[vi]:
            continue
        for task in [tk for tk in v.queue if tk.start_time is None and not tk.locked]:
            ui = min(nb[vi], key=lambda j: vdist(v, vehicles[j]))
            task.locked = True
            moves.append((task, vi, ui))
    return moves


def strategy_random(
    vehicles: List[Vehicle],
    nb:       List[List[int]],
    rng:      Mulberry32,
    prob:     float = 0.35,
) -> List[Move]:
    """
    Random offloading strategy with a fixed probability.

    For each unstarted task on each vehicle, independently tosses a biased coin
    (default 35% probability).  On success, picks a uniformly random neighbor
    as the offload target.  Serves as a stochastic lower-bound reference showing
    the effect of undirected offloading.

    Args:
        vehicles  List of active Vehicle objects.
        nb        Neighbor adjacency list from build_neighbors().
        rng       Seeded Mulberry32 RNG for reproducibility.
        prob      Probability of offloading each individual task (default 0.35).

    Returns:
        List of Move tuples for all randomly selected offloads.
    """
    moves: List[Move] = []
    for vi, v in enumerate(vehicles):
        for task in [tk for tk in v.queue if tk.start_time is None and not tk.locked]:
            if not nb[vi]:
                continue
            if rng() < prob:
                ui = nb[vi][rng.int_below(len(nb[vi]))]
                task.locked = True
                moves.append((task, vi, ui))
    return moves


def strategy_round_robin(
    vehicles: List[Vehicle],
    nb:       List[List[int]],
    rr_state: Dict,
) -> List[Move]:
    """
    Round-robin offloading strategy cycling through available neighbors.

    Only activates when a vehicle has more than one pending task.  Selects a
    neighbor by cycling a global counter (rr_state["idx"]) modulo the number of
    current neighbors, then offloads the first eligible task.  Provides uniform
    load distribution without any quality-of-link awareness.

    Args:
        vehicles  List of active Vehicle objects.
        nb        Neighbor adjacency list from build_neighbors().
        rr_state  Mutable dict with key "idx" holding the current round-robin
                  position; persists across simulation steps.

    Returns:
        List of Move tuples for all selected offloads.
    """
    moves: List[Move] = []
    for vi, v in enumerate(vehicles):
        pending = [tk for tk in v.queue if tk.start_time is None and not tk.locked]
        if len(pending) <= 1 or not nb[vi]:
            continue
        task = pending[0]
        ui   = nb[vi][rr_state["idx"] % len(nb[vi])]
        rr_state["idx"] += 1
        task.locked = True
        moves.append((task, vi, ui))
    return moves


