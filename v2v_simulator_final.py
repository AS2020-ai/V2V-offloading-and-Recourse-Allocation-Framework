def _expire_tasks(vehicles: List[Vehicle], t: float, done: List[Task]) -> None:
    """
    Expire all tasks that have passed their deadline without starting execution.

    For each vehicle, identifies tasks that are still queued (start_time is None)
    but whose deadline has elapsed.  These tasks are marked as missed, given a
    completion_time equal to the current time, cloned into the global done list,
    and removed from the vehicle's queue.

    Called at the start of every simulation step before new arrivals or offloading.

    Args:
        vehicles  List of active Vehicle objects for the current step.
        t         Current simulation time (seconds).
        done      Global list to which expired tasks are appended.
    """
    for v in vehicles:
        expired = [tk for tk in v.queue if tk.start_time is None and tk.deadline < t]
        for tk in expired:
            tk.completion_time = t
            tk.missed_deadline = True
            done.append(tk.clone())
        v.queue = [tk for tk in v.queue
                   if not (tk.start_time is None and tk.deadline < t)]


def _apply_offloads(
    moves:         List[Move],
    vehicles:      List[Vehicle],
    p:             SimParams,
    cum_comm_e:    List[float],
) -> None:
    """
    Execute all offload moves decided by the active strategy.

    For each (task, from_idx, to_idx) triple:
        1. Looks up source and target Vehicle objects by vid.
        2. Removes the task from the source queue, updates its assigned_to field,
           and appends it to the target queue.
        3. Computes the communication energy consumed (mJ) as:
               tx_power × transmission_time × 1000
           and accumulates it in cum_comm_e[0].

    Silently skips any move where the source or target no longer exists (possible
    in trace mode if a vehicle leaves the map mid-step) or where the task has
    already been moved by a previous move in the same step.

    Args:
        moves        List of Move tuples from _dispatch().
        vehicles     List of active Vehicle objects.
        p            SimParams (uses bandwidth, tx_power, noise_floor).
        cum_comm_e   Single-element list acting as a mutable float accumulator.
    """
    v_by_idx = {v.vid: v for v in vehicles}
    for task, from_idx, to_idx in moves:
        fv = v_by_idx.get(from_idx)
        tv = v_by_idx.get(to_idx)
        if fv is None or tv is None or task not in fv.queue:
            continue
        fv.queue.remove(task)
        task.assigned_to = tv.trace_id
        task.offloaded   = True
        task.locked      = False
        tv.queue.append(task)
        d    = vdist(fv, tv)
        rate = channel_rate(d, p.bandwidth, p.tx_power, p.noise_floor)
        cum_comm_e[0] += p.tx_power * tx_seconds(task.data_size, rate) * 1e3


def _process_fcfs(vehicles: List[Vehicle], t: float, dt: float, done: List[Task]) -> None:
    """
    Advance task processing for all vehicles by one simulation step using FCFS scheduling.

    For each vehicle with a non-empty queue:
        1. Sets start_time on the head-of-queue task if not already set.
        2. Increments the accumulated processing time for that task by dt.
        3. Increments the vehicle's busy_time by dt.
        4. If accumulated time ≥ cycles/cpu (task is complete), records
           completion_time = t + dt, flags missed_deadline if late, clones
           the task into done, removes it from the queue, and clears its
           proc_accum entry.

    Only the head of each queue is processed per step, enforcing strict FCFS order.

    Args:
        vehicles  List of active Vehicle objects.
        t         Current simulation time (seconds).
        dt        Simulation time step duration (seconds).
        done      Global list to which completed tasks are appended.
    """
    for v in vehicles:
        if not v.queue:
            continue
        task = v.queue[0]
        if task.start_time is None:
            task.start_time = t
        if task.task_id not in v.proc_accum:
            v.proc_accum[task.task_id] = 0.0
        v.proc_accum[task.task_id] += dt
        v.busy_time += dt
        if v.proc_accum[task.task_id] >= task.cycles / v.cpu:
            task.completion_time = t + dt
            task.missed_deadline = task.completion_time > task.deadline
            done.append(task.clone())
            v.queue.pop(0)
            del v.proc_accum[task.task_id]


def _dispatch(
    strategy:     str,
    vehicles:     List[Vehicle],
    nb:           List[List[int]],
    p:            SimParams,
    t:            float,
    rng_strategy: Mulberry32,
    rr_state:     Dict,
) -> List[Move]:
    """
    Route the current simulation step to the appropriate offloading strategy function.

    Acts as a single dispatch point so both simulation engines (synthetic and
    trace-based) call one function regardless of which strategy is active.
    LOCAL_ONLY returns an empty list, meaning no tasks are ever moved.

    Args:
        strategy      String key identifying the chosen strategy.
        vehicles      List of active Vehicle objects.
        nb            Neighbor adjacency list from build_neighbors().
        p             SimParams passed through to strategy functions.
        t             Current simulation time (seconds).
        rng_strategy  Dedicated RNG instance for strategy randomness only
                      (separate from task-generation RNG, used only by RANDOM).
        rr_state      Round-robin counter dict (used only by ROUND_ROBIN strategy).

    Returns:
        List of Move tuples produced by the selected strategy (empty for LOCAL_ONLY).
    """
    if   strategy == "V2V_BASELINE": return strategy_v2v_baseline(vehicles, nb, p)
    elif strategy == "NEAREST":      return strategy_nearest(vehicles, nb)
    elif strategy == "RANDOM":       return strategy_random(vehicles, nb, rng_strategy)
    elif strategy == "ROUND_ROBIN":  return strategy_round_robin(vehicles, nb, rr_state)
    return []  # LOCAL_ONLY


def _snapshot(
    t:          float,
    vehicles:   List[Vehicle],
    done:       List[Task],
    cum_comm_e: float,
) -> SnapMetrics:
    """
    Compute a SnapMetrics object summarising the current simulation state.

    Calculates all time-series metrics from the global done list and the current
    vehicle list.  Called every 5 steps by both simulation engines to populate
    the snapshots list used for line chart generation.

    Args:
        t           Current simulation time (seconds).
        vehicles    List of active Vehicle objects.
        done        Global list of all finished (or expired) tasks so far.
        cum_comm_e  Cumulative communication energy consumed so far (mJ).

    Returns:
        A SnapMetrics instance with all fields populated for time t.
    """
    fin    = [tk for tk in done if not tk.missed_deadline]
    missed = [tk for tk in done if tk.missed_deadline]
    delays = [tk.completion_time - tk.arrival_time for tk in fin if tk.completion_time]
    avg_d  = sum(delays) / len(delays) if delays else 0.0
    N      = max(len(vehicles), 1)
    avg_q  = sum(len(v.queue) for v in vehicles) / N
    avg_u  = sum(v.busy_time / max(t + 0.001, 0.001) for v in vehicles) / N
    td     = len(done)
    return SnapMetrics(
        time           = round(t, 1),
        avg_delay      = round(avg_d, 4),
        completed      = len(fin),
        missed         = len(missed),
        total          = td,
        deadline_ratio = round(len(fin) / td, 3) if td else 0.0,
        avg_queue      = round(avg_q, 2),
        cpu_util       = round(avg_u * 100.0, 1),
        comm_energy    = round(cum_comm_e, 2),
        comm_per_task  = round(cum_comm_e / len(fin), 3) if fin else 0.0,
    )


def _final_summary(
    done:        List[Task],
    busy_times:  Dict[str, float],
    T:           float,
    cum_comm_e:  float,
    total_veh:   int,
    n_generated: int,
) -> FinalSummary:
    """
    Compute the end-of-simulation FinalSummary from the completed task list.

    Aggregates all per-task data into the scalar metrics reported in the console
    summary table and bar charts.

    Args:
        done         Global list of all tasks that have left the active queue.
        busy_times   Dict mapping each vehicle's trace_id to its total busy_time (s).
        T            Total simulation duration (seconds); used for CPU utilisation.
        cum_comm_e   Total communication energy consumed during the run (mJ).
        total_veh    Total number of vehicles (denominator for CPU utilisation).
        n_generated  Total tasks created by the Poisson process (= tid at end of
                     simulation); must be identical across all strategies.

    Returns:
        A FinalSummary instance with all aggregate metrics populated.
    """
    fin    = [tk for tk in done if not tk.missed_deadline]
    delays = [tk.completion_time - tk.arrival_time for tk in fin if tk.completion_time]
    avg_d  = sum(delays) / len(delays) if delays else 0.0
    offl   = sum(1 for tk in fin if tk.offloaded)
    avg_u  = sum(busy_times.values()) / max(T * max(total_veh, 1), 0.001)
    return FinalSummary(
        generated     = n_generated,
        total         = len(done),
        completed     = len(fin),
        missed        = len(done) - len(fin),
        in_queue      = n_generated - len(done),
        avg_delay     = round(avg_d, 4),
        deadline_sat  = round(len(fin) / len(done) * 100.0, 1) if done else 0.0,
        offload_ratio = round(offl / len(fin) * 100.0, 1) if fin else 0.0,
        comm_energy   = round(cum_comm_e, 2),
        comm_per_task = round(cum_comm_e / len(fin), 3) if fin else 0.0,
        cpu_util      = round(avg_u * 100.0, 1),
    )


