def simulate_synthetic(
    p:        SimParams,
    strategy: str,
) -> Tuple[List[SnapMetrics], FinalSummary]:
    """
    Run a full simulation using procedurally generated (random-waypoint) mobility.

    Vehicles are initialised with random positions and velocities within the
    configured map_size.  Each step, positions are advanced by vx*dt / vy*dt
    and reflected off map boundaries (elastic bounce), keeping all vehicles
    within the arena throughout the simulation.

    Two separate RNG streams are used so that every strategy sees an identical
    task workload regardless of how many RNG calls the offloading logic makes:
        rng_tasks    (seed)     drives vehicle init, Poisson draws, task params.
        rng_strategy (seed + 1) drives RANDOM strategy offload decisions only.

    The task pipeline per step is identical to simulate_with_trace():
        expiry → generation → neighbors → dispatch → offload → FCFS processing.

    Args:
        p         SimParams controlling all simulation parameters.
        strategy  Strategy name string passed to _dispatch().

    Returns:
        (snapshots, summary) — list of per-step SnapMetrics and a FinalSummary.
    """
    rng_tasks    = Mulberry32(p.seed)
    rng_strategy = Mulberry32(p.seed + 1)

    # Initialise vehicles at random positions with random velocities
    vehicles = [
        Vehicle(
            vid=i, trace_id=str(i),
            x   = rng_tasks() * p.map_size,
            y   = rng_tasks() * p.map_size,
            vx  = (rng_tasks() - 0.5) * p.max_speed * 2.0,
            vy  = (rng_tasks() - 0.5) * p.max_speed * 2.0,
            cpu = (p.cpu_min + rng_tasks() * (p.cpu_max - p.cpu_min)) * 1e9,
        )
        for i in range(p.N)
    ]

    done:      List[Task]        = []
    snapshots: List[SnapMetrics] = []
    tid        = 0
    cum_comm_e = [0.0]
    rr_state   = {"idx": 0}
    steps      = round(p.T / p.dt)

    for step in range(steps):
        t = step * p.dt
        N = len(vehicles)

        # Move all vehicles and apply elastic boundary reflection
        for v in vehicles:
            v.x += v.vx * p.dt
            v.y += v.vy * p.dt
            if v.x <= 0.0 or v.x >= p.map_size:
                v.vx *= -1.0
                v.x = max(0.0, min(p.map_size, v.x))
            if v.y <= 0.0 or v.y >= p.map_size:
                v.vy *= -1.0
                v.y = max(0.0, min(p.map_size, v.y))

        _expire_tasks(vehicles, t, done)

        for _ in range(poisson_draw(p.arrival_rate * p.dt, rng_tasks)):
            owner = vehicles[rng_tasks.int_below(N)]
            owner.queue.append(make_task(tid, owner, t, p, rng_tasks))
            tid += 1

        nb    = build_neighbors(vehicles, p.comm_range)
        moves = _dispatch(strategy, vehicles, nb, p, t, rng_strategy, rr_state)
        _apply_offloads(moves, vehicles, p, cum_comm_e)

        for v in vehicles:
            for tk in v.queue:
                tk.locked = False

        _process_fcfs(vehicles, t, p.dt, done)

        if step % 5 == 0:
            snapshots.append(_snapshot(t, vehicles, done, cum_comm_e[0]))

    busy = {str(v.vid): v.busy_time for v in vehicles}
    summary = _final_summary(done, busy, p.T, cum_comm_e[0], p.N, tid)
    return snapshots, summary


