ALL_STRATEGIES = [
    "V2V_BASELINE", "LOCAL_ONLY", "RANDOM", "NEAREST", "ROUND_ROBIN"
]


def build_parser() -> argparse.ArgumentParser:
    """
    Construct the command-line argument parser for the simulator.

    Organises arguments into three groups:
        (default)   Trace file path, strategy selection, output directory, plot toggle.
        Simulation  Vehicle count, duration, time step, seed, comm range, map size.
        Tasks       Arrival rate, data size bounds, cycle bounds, deadline bounds,
                    and CPU frequency bounds.

    Returns:
        A configured ArgumentParser ready for parse_args().
    """
    ap = argparse.ArgumentParser(
        description="V2V Task Offloading Simulator",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    ap.add_argument("--trace",       type=str, default=None,
                    help="SUMO FCD .xml or CSV trace (omit for synthetic mode)")
    ap.add_argument("--strategies",  nargs="+",
                    default=["V2V_BASELINE", "LOCAL_ONLY", "RANDOM", "NEAREST", "ROUND_ROBIN"],
                    choices=ALL_STRATEGIES, metavar="S")
    ap.add_argument("--output-dir",  type=str, default="v2v_results")
    ap.add_argument("--no-plots",    action="store_true")

    sim = ap.add_argument_group("Simulation")
    sim.add_argument("--N",            type=int,   default=25)
    sim.add_argument("--T",            type=float, default=120.0)
    sim.add_argument("--dt",           type=float, default=0.5)
    sim.add_argument("--seed",         type=int,   default=42)
    sim.add_argument("--comm-range",   type=float, default=100.0)
    sim.add_argument("--map-size",     type=float, default=500.0)
    sim.add_argument("--max-speed",    type=float, default=20.0)

    tsk = ap.add_argument_group("Tasks")
    tsk.add_argument("--arrival-rate", type=float, default=8.0)
    tsk.add_argument("--ds-min",       type=float, default=100.0)
    tsk.add_argument("--ds-max",       type=float, default=1000.0)
    tsk.add_argument("--cycles-min",   type=float, default=500.0)
    tsk.add_argument("--cycles-max",   type=float, default=2000.0)
    tsk.add_argument("--dl-min",       type=float, default=2.0)
    tsk.add_argument("--dl-max",       type=float, default=6.0)
    tsk.add_argument("--cpu-min",      type=float, default=0.5)
    tsk.add_argument("--cpu-max",      type=float, default=1.5)
    return ap


def main() -> None:
    """
    Entry point for the V2V Task Offloading Simulator.

    Workflow:
        1. Parse CLI arguments and populate a SimParams instance.
        2. If a trace file is provided, detect its format (XML or CSV) and parse it;
           override p.T and p.dt with the trace's actual duration and time step.
        3. Run each requested strategy through the appropriate simulation engine
           (trace-based or synthetic) and collect (snapshots, summary) results.
        4. Print a formatted summary table to stdout.
        5. Generate and save PNG charts unless --no-plots is set.
    """
    args = build_parser().parse_args()

    p = SimParams(
        N=args.N, T=args.T, dt=args.dt, seed=args.seed,
        comm_range=args.comm_range, map_size=args.map_size, max_speed=args.max_speed,
        arrival_rate=args.arrival_rate,
        ds_min=args.ds_min, ds_max=args.ds_max,
        cycles_min=args.cycles_min, cycles_max=args.cycles_max,
        deadline_min=args.dl_min, deadline_max=args.dl_max,
        cpu_min=args.cpu_min, cpu_max=args.cpu_max,
    )

    trace: Optional[MobilityTrace] = None
    if args.trace:
        print(f"\nLoading trace: {args.trace}")
        ext   = os.path.splitext(args.trace)[1].lower()
        trace = parse_sumo_fcd(args.trace) if ext in (".xml", ".fcd") else parse_csv_trace(args.trace)
        geo   = "  [geo→metres]" if trace.is_geo else ""
        print(f"  {trace.N} vehicles  T={trace.T:.1f}s  dt={trace.dt:.2f}s"
              f"  map={trace.map_w:.0f}×{trace.map_h:.0f}m{geo}")
        p.T, p.dt = trace.T, trace.dt
    else:
        print(f"\nSynthetic mode  N={p.N}  T={p.T}s  seed={p.seed}")

    print()
    results: Dict[str, Tuple[List[SnapMetrics], FinalSummary]] = {}
    for s in args.strategies:
        print(f"  [{s:<14}] running…", end="  ", flush=True)
        snaps, sm = (simulate_with_trace(p, s, trace)
                     if trace else simulate_synthetic(p, s))
        results[s] = (snaps, sm)
        print(f"done  completed={sm.completed}  DL_sat={sm.deadline_sat}%"
              f"  CommE/task={sm.comm_per_task:.3f} mJ")

    print_summary(results)

    if not args.no_plots:
        print(f"Generating charts → {args.output_dir}/")
        plot_results(results, output_dir=args.output_dir)


if __name__ == "__main__":
    main()
