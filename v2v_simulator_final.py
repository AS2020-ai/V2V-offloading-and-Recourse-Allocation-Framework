def simulate_with_trace(
    p:        SimParams,
    strategy: str,
    trace:    MobilityTrace,
) -> Tuple[List[SnapMetrics], FinalSummary]:
    """
    Run a full simulation using real vehicle mobility data from a MobilityTrace.

    Each step reconstructs the active vehicle list by interpolating positions from
    the trace, then runs the full pipeline: task expiry → task generation →
    neighbor graph → offloading → processing.  Vehicle state (queue, busy_time,
    proc_accum) is persisted across steps using per-ID dictionaries, because the
    Vehicle objects themselves are rebuilt each step from trace data.

    Two separate RNG streams are used so that every strategy sees an identical
    task workload regardless of how many RNG calls the offloading logic makes:
        rng_tasks    (seed)     drives CPU assignment, Poisson draws, task params.
        rng_strategy (seed + 1) drives RANDOM strategy offload decisions only.

    The arrival rate is scaled by (active_vehicles / total_ids) each step so
    the overall task load remains proportional to participation density.

    Args:
        p         SimParams controlling all simulation parameters.
        strategy  Strategy name string passed to _dispatch().
        trace     Parsed MobilityTrace from parse_sumo_fcd() or parse_csv_trace().

    Returns:
        (snapshots, summary) — list of per-step SnapMetrics and a FinalSummary.
    """
    rng_tasks    = Mulberry32(p.seed)
    rng_strategy = Mulberry32(p.seed + 1)

    # Assign a fixed random CPU frequency to each vehicle ID at the start
    cpu_map: Dict[str, float] = {
        vid: (p.cpu_min + rng_tasks() * (p.cpu_max - p.cpu_min)) * 1e9
        for vid in trace.ids
    }

    # Persistent state dicts keyed by vehicle trace_id
    v_queues:     Dict[str, List[Task]]       = defaultdict(list)
    v_busy_time:  Dict[str, float]            = defaultdict(float)
    v_proc_accum: Dict[str, Dict[int, float]] = defaultdict(dict)

    done:       List[Task]        = []
    snapshots:  List[SnapMetrics] = []
    tid         = 0
    cum_comm_e  = [0.0]
    rr_state    = {"idx": 0}
    steps       = round(trace.T / trace.dt)
    total_ids   = len(trace.ids)

    for step in range(steps):
        t     = step * trace.dt
        t_abs = t + trace.t0

        # Rebuild the active vehicle list for this step via interpolation
        vehicles: List[Vehicle] = []
        for idx, vid in enumerate(trace.ids):
            pos = _interpolate(trace.vehicle_map[vid], t_abs, trace.dt)
            if pos is None:
                continue
            x, y, vx, vy = pos
            vehicles.append(Vehicle(
                vid=idx, trace_id=vid, x=x, y=y, vx=vx, vy=vy,
                cpu=cpu_map[vid],
                queue=v_queues[vid],
                busy_time=v_busy_time[vid],
                proc_accum=v_proc_accum[vid],
            ))

        N = len(vehicles)
        if N == 0:
            continue

        _expire_tasks(vehicles, t, done)

        # Scale arrival rate by fraction of vehicles currently active in the trace
        lam = p.arrival_rate * trace.dt * (N / max(total_ids, 1))
        for _ in range(poisson_draw(lam, rng_tasks)):
            owner = vehicles[rng_tasks.int_below(N)]
            owner.queue.append(make_task(tid, owner, t, p, rng_tasks))
            tid += 1

        nb    = build_neighbors(vehicles, p.comm_range)
        moves = _dispatch(strategy, vehicles, nb, p, t, rng_strategy, rr_state)
        _apply_offloads(moves, vehicles, p, cum_comm_e)

        # Unlock all tasks before next step
        for v in vehicles:
            for tk in v.queue:
                tk.locked = False

        _process_fcfs(vehicles, t, trace.dt, done)

        # Flush transient Vehicle state back to persistent dicts
        for v in vehicles:
            v_queues[v.trace_id]     = v.queue
            v_busy_time[v.trace_id]  = v.busy_time
            v_proc_accum[v.trace_id] = v.proc_accum

        if step % 5 == 0:
            snapshots.append(_snapshot(t, vehicles, done, cum_comm_e[0]))

    summary = _final_summary(done, dict(v_busy_time), trace.T, cum_comm_e[0], len(trace.ids), tid)
    return snapshots, summary


