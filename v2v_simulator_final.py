# PHYSICS HELPERS
# ═══════════════════════════════════════════════════════════════════════════

def vdist(a: Vehicle, b: Vehicle) -> float:
    """
    Compute the Euclidean distance (metres) between two vehicles.

    Used by build_neighbors() to test communication range, by the offloading
    strategies to find the nearest neighbor, and by channel_rate() to derive SNR.
    """
    return math.sqrt((a.x - b.x) ** 2 + (a.y - b.y) ** 2)


def channel_rate(d: float, bw: float, tx_p: float, noise: float) -> float:
    """
    Estimate the V2V wireless channel capacity (bits/s) using the Shannon formula.

    Args:
        d      Distance between vehicles (m); clamped to 1 m minimum.
        bw     Channel bandwidth (Hz).
        tx_p   Transmit power (W).
        noise  Thermal noise floor (W).

    Returns:
        Shannon capacity in bits per second.
    """
    d = max(d, 1.0)
    snr = tx_p / (noise * (d / 10.0) ** 3.5)
    return bw * math.log2(1.0 + max(snr, 1e-10))


def tx_seconds(data_bytes: float, rate_bps: float) -> float:
    """
    Convert a payload size to transmission time (seconds).

    Args:
        data_bytes  Payload size in bytes.
        rate_bps    Channel rate in bits per second.

    Returns:
        Transmission duration in seconds.
    """
    return data_bytes * 8.0 / rate_bps


def build_neighbors(vehicles: List[Vehicle], comm_range: float) -> List[List[int]]:
    """
    Build an adjacency list representing the V2V communication graph.

    Iterates over all vehicle pairs and adds a bidirectional edge whenever the
    inter-vehicle distance is within comm_range.  The returned list is indexed
    by vehicle position in the vehicles list (i.e., nb[i] contains the indices
    of all vehicles within range of vehicles[i]).

    Args:
        vehicles    List of active Vehicle objects for the current time step.
        comm_range  Maximum communication range in metres.

    Returns:
        nb  — list of neighbor index lists, one per vehicle.
    """
    n = len(vehicles)
    nb: List[List[int]] = [[] for _ in range(n)]
    for i in range(n):
        for j in range(i + 1, n):
            # Add an undirected edge if the pair is within communication range
            if vdist(vehicles[i], vehicles[j]) <= comm_range:
                nb[i].append(j)
                nb[j].append(i)
    return nb


def queue_wait(task: Task, vehicle: Vehicle) -> float:
    """
    Estimate the queuing delay (seconds) a task would experience on a vehicle.

    Sums the remaining execution time of all tasks ahead of the given task in
    the vehicle's FCFS queue (i.e., tasks with smaller task_id that are not yet
    complete).  Used by expected_delay() as the waiting component.

    Args:
        task     The task whose wait time is being calculated.
        vehicle  The vehicle whose queue is being examined.

    Returns:
        Total wait time in seconds before the task can begin execution.
    """
    return sum(
        tk.cycles / vehicle.cpu
        for tk in vehicle.queue
        if tk.task_id < task.task_id and tk.completion_time is None
    )


def expected_delay(task: Task, vehicle: Vehicle, tx: float = 0.0) -> float:
    """
    Estimate the total end-to-end delay (seconds) of executing a task on a vehicle.

    Combines three components:
        tx           Transmission time to reach the target vehicle (0 for local).
        queue_wait   Time spent waiting behind already-queued tasks.
        cycles/cpu   Actual execution time on the vehicle's CPU.

    Used by all offloading strategies to compare local vs. remote execution cost.

    Args:
        task     The task being evaluated.
        vehicle  The candidate vehicle (local or remote neighbor).
        tx       Transmission time in seconds (default 0 for local execution).

    Returns:
        Estimated total delay from now until task completion (seconds).
    """
    return tx + queue_wait(task, vehicle) + task.cycles / vehicle.cpu


