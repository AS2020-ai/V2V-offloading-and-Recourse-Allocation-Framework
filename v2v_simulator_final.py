# SEEDED RNG  — Mulberry32 (identical stream to the JS simulator)
# ═══════════════════════════════════════════════════════════════════════════

class Mulberry32:
    """
    Deterministic pseudo-random number generator using the Mulberry32 algorithm.

    Chosen because it produces a bit-identical random stream to the JavaScript
    simulator, enabling reproducible cross-platform comparisons with the same seed.

    Usage:
        rng = Mulberry32(seed=42)
        value  = rng()            # float in [0, 1)
        index  = rng.int_below(n) # integer in [0, n)
    """

    def __init__(self, seed: int) -> None:
        """Initialise the internal 32-bit state from the provided seed."""
        self._s = int(seed) & 0xFFFFFFFF

    def __call__(self) -> float:
        """Advance the state by one step and return a float in [0, 1)."""
        self._s = (self._s + 0x6D2B79F5) & 0xFFFFFFFF
        t = (self._s ^ (self._s >> 15)) & 0xFFFFFFFF
        t = (t * ((1 | self._s) & 0xFFFFFFFF)) & 0xFFFFFFFF
        inner = (((t ^ (t >> 7)) & 0xFFFFFFFF) * ((61 | t) & 0xFFFFFFFF)) & 0xFFFFFFFF
        t = (t ^ (t + inner)) & 0xFFFFFFFF
        return ((t ^ (t >> 14)) & 0xFFFFFFFF) / 4_294_967_296.0

    def int_below(self, n: int) -> int:
        """Return a random integer in the range [0, n)."""
        return int(self() * n)


