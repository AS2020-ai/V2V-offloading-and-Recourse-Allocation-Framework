@dataclass
class Task:
    """
    Represents a single computational task generated by a vehicle.

    Lifecycle:
        1. Created by make_task() and placed in the owning vehicle's queue.
        2. Optionally offloaded to a neighbor — assigned_to changes, offloaded=True.
        3. Processed FCFS by _process_fcfs(); start_time and completion_time are set.
        4. Cloned into the global 'done' list once finished or deadline-missed.

    Fields:
        task_id          Unique monotonically increasing identifier.
        origin           trace_id of the vehicle that generated the task.
        assigned_to      trace_id of the vehicle currently responsible for execution.
        data_size        Payload in bytes; determines transmission time.
        cycles           CPU cycles required; combined with vehicle CPU to get exec time.
        deadline         Absolute simulation time by which the task must complete.
        arrival_time     Simulation time when the task was created.
        start_time       Set when processing begins (None if still queued).
        completion_time  Set when execution finishes or deadline expires.
        offloaded        True if the task was transferred to a different vehicle.
        missed_deadline  True if completion_time > deadline.
        locked           Temporary flag preventing double-offloading in one step.
    """
    task_id:         int
    origin:          str
    assigned_to:     str
    data_size:       float
    cycles:          float
    deadline:        float
    arrival_time:    float
    start_time:      Optional[float] = None
    completion_time: Optional[float] = None
    offloaded:       bool = False
    missed_deadline: bool = False
    locked:          bool = False

    def clone(self) -> "Task":
        """Return a shallow copy of this task, used when archiving to the done list."""
        return copy.copy(self)


