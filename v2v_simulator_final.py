@dataclass
class Frame:
    """
    A single position record for one vehicle at one timestep in a mobility trace.

    Fields:
        t    Absolute simulation time (seconds).
        x    X coordinate (metres after geo-conversion, or raw metres from SUMO).
        y    Y coordinate (metres).
        spd  Instantaneous speed (m/s); derived from positional diff if missing.
    """
    t:   float
    x:   float
    y:   float
    spd: float


@dataclass
class MobilityTrace:
    """
    Parsed and normalised mobility dataset ready for use by simulate_with_trace().

    Fields:
        vehicle_map  Maps each vehicle ID string to its sorted list of Frames.
        t0           Start timestamp of the trace (seconds); used for time alignment.
        T            Total trace duration in seconds (last_time - t0).
        dt           Median inter-frame interval (seconds); becomes the simulation step.
        map_w        Bounding-box width in metres.
        map_h        Bounding-box height in metres.
        ids          Ordered list of all vehicle ID strings.
        is_geo       True if source coordinates were geographic (lat/lon) and
                     have been projected to metres by _finalise_trace().
    """
    vehicle_map: Dict[str, List[Frame]]
    t0:          float
    T:           float
    dt:          float
    map_w:       float
    map_h:       float
    ids:         List[str]
    is_geo:      bool = False

    @property
    def N(self) -> int:
        """Total number of vehicles in the trace."""
        return len(self.ids)


def _finalise_trace(vehicle_map: Dict[str, List[Frame]]) -> MobilityTrace:
    """
    Validate, sort, and normalise a raw vehicle_map into a MobilityTrace.

    Steps performed:
        1. Sort each vehicle's frame list by ascending time.
        2. Infer the median time step (dt) from the first 20 inter-frame gaps.
        3. Detect geographic coordinates (lat/lon) by checking if x values are
           within [-180, 180] and the span is less than 5 degrees.
        4. If geographic, project to metres using the Equirectangular approximation
           centred on the midpoint latitude.
        5. Back-fill missing speed values using positional differences.
        6. Compute bounding-box dimensions and absolute start time t0.

    Args:
        vehicle_map  Raw dict mapping vehicle ID → list of Frame objects.

    Returns:
        A fully constructed MobilityTrace ready for _interpolate() lookups.

    Raises:
        ValueError  If no vehicles are found or fewer than 2 distinct timesteps exist.
    """
    if not vehicle_map:
        raise ValueError("No vehicles found in trace.")

    ids = list(vehicle_map.keys())
    for vid in ids:
        vehicle_map[vid].sort(key=lambda f: f.t)

    all_times = sorted({f.t for frames in vehicle_map.values() for f in frames})
    if len(all_times) < 2:
        raise ValueError("Trace needs at least 2 distinct timesteps.")

    # Derive median dt from first 20 inter-frame gaps for robustness
    dts = [all_times[i + 1] - all_times[i] for i in range(min(20, len(all_times) - 1))]
    dt  = sorted(dts)[len(dts) // 2]

    all_x = [f.x for frames in vehicle_map.values() for f in frames]
    all_y = [f.y for frames in vehicle_map.values() for f in frames]

    # Detect geographic (lat/lon) coordinates
    is_geo = (abs(all_x[0]) < 180 and abs(all_y[0]) < 90
              and (max(all_x) - min(all_x)) < 5.0)

    if is_geo:
        # Project lat/lon to metres using Equirectangular approximation
        lat_mid       = (min(all_y) + max(all_y)) / 2.0
        m_per_deg_lon = 111_320.0 * math.cos(math.radians(lat_mid))
        m_per_deg_lat = 110_540.0
        x_min, y_min  = min(all_x), min(all_y)
        for frames in vehicle_map.values():
            for f in frames:
                f.x = (f.x - x_min) * m_per_deg_lon
                f.y = (f.y - y_min) * m_per_deg_lat
        all_x = [f.x for frames in vehicle_map.values() for f in frames]
        all_y = [f.y for frames in vehicle_map.values() for f in frames]

    # Back-fill missing speeds using positional differences
    for frames in vehicle_map.values():
        for i, f in enumerate(frames):
            if f.spd == 0.0 and i > 0:
                prev = frames[i - 1]
                d    = math.sqrt((f.x - prev.x) ** 2 + (f.y - prev.y) ** 2)
                f.spd = d / max(f.t - prev.t, 0.01)

    t0 = all_times[0]
    return MobilityTrace(
        vehicle_map = vehicle_map,
        t0  = t0,
        T   = all_times[-1] - t0,
        dt  = dt,
        map_w = max(all_x) - min(all_x) + 1.0,
        map_h = max(all_y) - min(all_y) + 1.0,
        ids = ids,
        is_geo = is_geo,
    )


def parse_sumo_fcd(path: str) -> MobilityTrace:
    """
    Parse a SUMO Floating Car Data (FCD) XML file into a MobilityTrace.

    Expects the standard SUMO FCD format produced by --fcd-output:
        <fcd-export>
          <timestep time="0.0">
            <vehicle id="v0" x="..." y="..." speed="..."/>
            ...
          </timestep>
          ...
        </fcd-export>

    Accepts both 'speed' and 'spd' as the speed attribute name.

    Args:
        path  Filesystem path to the .fcd.xml file.

    Returns:
        A normalised MobilityTrace via _finalise_trace().

    Raises:
        ValueError  If the file contains no <vehicle> elements.
    """
    tree = ET.parse(path)
    root = tree.getroot()
    vehicle_map: Dict[str, List[Frame]] = {}

    for ts in root.findall("timestep"):
        t = float(ts.get("time", 0))
        for v in ts.findall("vehicle"):
            vid = v.get("id", "")
            x   = float(v.get("x", 0))
            y   = float(v.get("y", 0))
            spd = float(v.get("speed") or v.get("spd") or 0)
            if vid not in vehicle_map:
                vehicle_map[vid] = []
            vehicle_map[vid].append(Frame(t=t, x=x, y=y, spd=spd))

    if not vehicle_map:
        raise ValueError("No <vehicle> elements found in FCD file.")
    return _finalise_trace(vehicle_map)


def parse_csv_trace(path: str) -> MobilityTrace:
    """
    Parse a generic CSV mobility trace (TAPASCologne, LuST, or custom) into a MobilityTrace.

    Auto-detects the delimiter (comma vs semicolon) by scanning the first 2 KB.
    Column names are matched case-insensitively against known aliases for ID,
    time, X, Y, and speed.  Speed is optional; if absent, _finalise_trace()
    back-fills it from positional differences.

    Supported column name aliases:
        ID:    id, vehicle_id, vehicleid, node, car, name
        Time:  time, t, timestamp, timestep, step
        X:     x, lon, longitude, pos_x, posx, easting
        Y:     y, lat, latitude, pos_y, posy, northing
        Speed: speed, v, velocity, spd, vel

    Args:
        path  Filesystem path to the CSV file.

    Returns:
        A normalised MobilityTrace via _finalise_trace().

    Raises:
        ValueError  If any required column (ID, time, X, Y) cannot be found.
    """
    ID_COLS   = ["id", "vehicle_id", "vehicleid", "node", "car", "name"]
    TIME_COLS = ["time", "t", "timestamp", "timestep", "step"]
    X_COLS    = ["x", "lon", "longitude", "pos_x", "posx", "easting"]
    Y_COLS    = ["y", "lat", "latitude",  "pos_y", "posy", "northing"]
    SPD_COLS  = ["speed", "v", "velocity", "spd", "vel"]

    def find_col(hl: List[str], names: List[str]) -> int:
        """Return the index of the first matching column name, or -1 if not found."""
        for n in names:
            if n in hl:
                return hl.index(n)
        return -1

    # Detect delimiter from a small sample of the file
    with open(path, newline="", encoding="utf-8-sig") as fh:
        sample = fh.read(2048)
    delim = ";" if sample.count(";") > sample.count(",") else ","

    vehicle_map: Dict[str, List[Frame]] = {}

    with open(path, newline="", encoding="utf-8-sig") as fh:
        reader  = csv.reader(fh, delimiter=delim)
        headers = [h.strip() for h in next(reader)]
        hl      = [h.lower() for h in headers]

        ci_id   = find_col(hl, ID_COLS)
        ci_time = find_col(hl, TIME_COLS)
        ci_x    = find_col(hl, X_COLS)
        ci_y    = find_col(hl, Y_COLS)
        ci_spd  = find_col(hl, SPD_COLS)

        missing = [name for name, idx in
                   [("ID", ci_id), ("time", ci_time), ("X", ci_x), ("Y", ci_y)]
                   if idx < 0]
        if missing:
            raise ValueError(f"Cannot find column(s) {missing}. Headers: {headers}")

        for row in reader:
            if len(row) < 3:
                continue
            try:
                vid = row[ci_id].strip()
                t   = float(row[ci_time])
                x   = float(row[ci_x])
                y   = float(row[ci_y])
                spd = float(row[ci_spd]) if ci_spd >= 0 else 0.0
            except (ValueError, IndexError):
                continue
            if vid not in vehicle_map:
                vehicle_map[vid] = []
            vehicle_map[vid].append(Frame(t=t, x=x, y=y, spd=spd))

    return _finalise_trace(vehicle_map)


def _interpolate(
    frames: List[Frame],
    t_abs:  float,
    dt:     float,
) -> Optional[Tuple[float, float, float, float]]:
    """
    Linearly interpolate a vehicle's position and velocity at an arbitrary time.

    Searches the frame list for the bracketing interval [lo, hi] and interpolates
    x and y.  Velocity components (vx, vy) are derived from the positional
    difference over the interval span, providing a piecewise-linear velocity model.

    Returns None if t_abs falls outside the vehicle's active period (with one dt
    of tolerance at either end), indicating the vehicle is not present at that time.

    Args:
        frames  Sorted list of Frame records for one vehicle.
        t_abs   Absolute simulation time to query.
        dt      Nominal time step, used as an outside-trace tolerance margin.

    Returns:
        (x, y, vx, vy) tuple in metres and m/s, or None if out of range.
    """
    if t_abs < frames[0].t - dt or t_abs > frames[-1].t + dt:
        return None
    lo, hi = frames[0], frames[-1]
    for i in range(len(frames) - 1):
        if frames[i].t <= t_abs <= frames[i + 1].t:
            lo, hi = frames[i], frames[i + 1]
            break
    span = max(hi.t - lo.t, 1e-6)
    frac = min(1.0, max(0.0, (t_abs - lo.t) / span))
    x  = lo.x + (hi.x - lo.x) * frac
    y  = lo.y + (hi.y - lo.y) * frac
    vx = (hi.x - lo.x) / span
    vy = (hi.y - lo.y) / span
    return x, y, vx, vy


