def poisson_draw(lam: float, rng: Mulberry32) -> int:
    """
    Draw a random integer from a Poisson distribution with mean lambda.

    Uses the Knuth algorithm (multiplicative method): generates uniform samples
    until their product falls below e^(-lambda).  This is numerically equivalent
    to counting events in a unit interval when the average rate is lambda.

    Used each simulation step to determine how many new tasks arrive across the
    vehicle population.

    Args:
        lam  Expected number of arrivals (lambda parameter).
        rng  Seeded Mulberry32 RNG instance.

    Returns:
        Number of task arrivals for this time step.
    """
    L, p, n = math.exp(-lam), 1.0, 0
    while True:
        p *= rng()
        n += 1
        if p <= L:
            break
    return n - 1


def make_task(tid: int, owner: Vehicle, t: float, p: SimParams, rng: Mulberry32) -> Task:
    """
    Instantiate a new Task with randomised parameters drawn from SimParams ranges.

    Data size, CPU cycles, and deadline offset are each sampled uniformly within
    the configured [min, max] bounds.  Data size is converted to bytes (×1e3) and
    cycles to raw cycle count (×1e6) so all arithmetic is in SI units.

    Args:
        tid    Unique task identifier (monotonically increasing counter).
        owner  The vehicle that generated the task (sets origin and initial assignment).
        t      Current simulation time (used as arrival_time and deadline base).
        p      SimParams holding the distribution bounds.
        rng    Seeded RNG for reproducible sampling.

    Returns:
        A fully initialised Task ready to be appended to the owner's queue.
    """
    return Task(
        task_id      = tid,
        origin       = owner.trace_id,
        assigned_to  = owner.trace_id,
        data_size    = (p.ds_min + rng() * (p.ds_max - p.ds_min)) * 1e3,
        cycles       = (p.cycles_min + rng() * (p.cycles_max - p.cycles_min)) * 1e6,
        deadline     = t + p.deadline_min + rng() * (p.deadline_max - p.deadline_min),
        arrival_time = t,
    )


