def print_summary(results: Dict[str, Tuple[List[SnapMetrics], FinalSummary]]) -> None:
    """
    Print a formatted comparison table of final metrics for all strategies to stdout.

    Columns: strategy name, average delay, deadline satisfaction %, completed tasks,
    missed deadlines, communication energy per task (mJ), and CPU utilisation %.

    Args:
        results  Dict mapping strategy name → (snapshots, summary), as returned
                 by the simulation engines.
    """
    hdr = (f"{'Strategy':<18} {'Generated':>10} {'InQueue':>8} {'Delay(s)':>9} {'DL Sat%':>8} "
           f"{'Done':>6} {'Missed':>7} {'CommE/Task':>11} {'CPU%':>6}")
    sep = "═" * len(hdr)
    print(f"\n{sep}\n{hdr}\n{'─'*len(hdr)}")
    for s, (_, sm) in results.items():
        print(f"{LABELS[s]:<18} {sm.generated:>10} {sm.in_queue:>8} {sm.avg_delay:>9.3f} "
              f"{sm.deadline_sat:>8.1f} {sm.completed:>6} {sm.missed:>7} "
              f"{sm.comm_per_task:>11.3f} {sm.cpu_util:>6.1f}")
    print(sep)
    print("  Generated = tasks created (must match across all strategies)")
    print("  InQueue   = tasks still queued at simulation end (Generated - Done)")
    print("  ★ CommE/Task = comm. energy per completed task (mJ) — KEY metric")
    print("    Comp. energy (~1000 mJ/task) is identical for all strategies and excluded.\n")


